/*
Entrar com um número e informar se ele é divisível por 10, por 5, por 2 ou se não é
divisível por nenhum destes.
 */

$n = 9;

if ($n >= 10) {
    echo $n. " é divisivel por 10";
}

elseif ($n >= 5) {
    echo $n. " é divisivel por 5";
}

elseif ($n >= 2) {
    echo $n. " é divisivel por 2";
}


